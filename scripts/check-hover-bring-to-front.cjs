const assert = require('assert')
const fs = require('fs')
const path = require('path')

function read(relativePath) {
  return fs.readFileSync(path.join(__dirname, '..', relativePath), 'utf8')
}

const defaultOptions = read('packages/gantt/src/config/defaultOptions.ts')
const types = read('packages/gantt/types/index.d.ts')
const interactions = read('packages/gantt/src/core/interactions.ts')
const measurements = read('packages/gantt/src/core/measurements.ts')
const ganttClass = read('packages/gantt/src/VanillaGantt.ts')
const distributionDemo = read('src/views/WorkOrderDistributionDemo.vue')

assert.match(defaultOptions, /hoverBringToFront:\s*false/, 'taskBar.hoverBringToFront defaults to false')
assert.match(defaultOptions, /showDelay:\s*0/, 'tooltip showDelay defaults to 0')
assert.match(types, /hoverBringToFront\?:\s*boolean/, 'public taskBar options expose hoverBringToFront')
assert.match(types, /showDelay\?:\s*number/, 'public tooltip options expose showDelay')
assert.match(interactions, /taskBar\.hoverBringToFront/, 'task hover promotion is gated by taskBar.hoverBringToFront')
assert.match(interactions, /appendChild\(node\)/, 'hovered task foreignObject is moved to the top of the SVG paint order')
assert.match(interactions, /__vgHoverOriginalNextSibling/, 'hover promotion stores the original sibling position')
assert.match(interactions, /insertBefore\(node,\s*nextSibling\)/, 'mouse leave restores the task foreignObject to its original paint order')
assert.match(interactions, /hoveredTaskNode/, 'hover interactions track the active task node')
assert.match(interactions, /gantt\.hoveredTaskKey === taskKey/, 'same task mouseover is ignored to avoid repeated tooltip work')
assert.match(interactions, /isPointerStillInsideNode/, 'spurious mouseout caused by DOM promotion is ignored')
assert.match(interactions, /vg-task-fo--hover/, 'hovered task receives a class for full-content styling')
assert.match(measurements, /tooltipPositionFrame/, 'tooltip movement is throttled through requestAnimationFrame')
assert.match(measurements, /pendingTooltipPosition/, 'tooltip keeps only the latest pointer position per frame')
assert.match(measurements, /tooltipShowTimer/, 'tooltip creation can be delayed and cancelled')
assert.match(measurements, /clearTooltipShowTimer/, 'pending tooltip show work is cancelled on hide')
assert.match(measurements, /milestoneTooltipKey/, 'milestone tooltip reuse key includes task and milestone identity')
assert.match(ganttClass, /tooltipPositionFrame = null/, 'gantt instance initializes tooltip positioning frame state')
assert.match(ganttClass, /tooltipShowTimer = null/, 'gantt instance initializes tooltip show timer state')
assert.match(ganttClass, /hoveredTaskNode = null[\s\S]*currentRenderSnapshot = null/, 'virtual cleanup clears stale hovered task state')
assert.match(distributionDemo, /hoverBringToFront:\s*true/, 'work order distribution demo enables hover promotion explicitly')
assert.match(distributionDemo, /showDelay:\s*80/, 'work order distribution demo delays tooltip creation during fast hover sweeps')
assert.match(distributionDemo, /vg-task-fo--hover[\s\S]*distribution-plan/, 'distribution demo expands plan content on hover')
assert.match(distributionDemo, /vg-task-fo--hover[\s\S]*distribution-logistics/, 'distribution demo expands logistics content on hover')

console.log('Checked configurable hover task promotion.')
